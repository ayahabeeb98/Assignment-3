     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $retriveData = $request->all();

        $note = new Note();
        $note->note_title = $retriveData['note_title'];
        $note->description = $retriveData['description'];

        $note->save();

        return redirect()->back()->with('success', 'Note has been saved successfully');

    }

